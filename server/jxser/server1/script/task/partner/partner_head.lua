function partner_parserbynpc(Tab)
	for i = 1 , getn(Tab) do 
		SId = SubWorldID2Idx(Tab[i][2]);
		if (SId >= 0) then
			npcindex = AddNpc(Tab[i][1],1,SId,Tab[i][3]*32,Tab[i][4]*32,1,Tab[i][6]);
			SetNpcScript(npcindex, Tab[i][5]);
		end;
	end	
end;

function partner_enemynpc(Tab3)
	for i = 1 , getn(Tab3) do
		Tid = SubWorldID2Idx(Tab3[i][3]);
		if (Tid >= 0 ) then
			TabValue4 = Tab3[i][4] * 32
			TabValue5 = Tab3[i][5] * 32
			newtasknpcindex = AddNpc(Tab3[i][1],Tab3[i][2],Tid,TabValue4,TabValue5,Tab3[i][6],Tab3[i][7],Tab3[i][8]);
			SetNpcScript(newtasknpcindex, Tab3[i][9]);
			SetNpcParam( newtasknpcindex,1,Tab3[i][10]);
		end;
	end;
end;

function add_masternpc(Tab4)
